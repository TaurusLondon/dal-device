package com.vf.uk.dal.device.datamodel.merchandisingpromotion;

public class OneOffDiscountPrice {

	private Float gross;
	private Float net;
	private Float vat;
	public Float getGross() {
		return gross;
	}
	public void setGross(Float gross) {
		this.gross = gross;
	}
	public Float getNet() {
		return net;
	}
	public void setNet(Float net) {
		this.net = net;
	}
	public Float getVat() {
		return vat;
	}
	public void setVat(Float vat) {
		this.vat = vat;
	}
	
}
