package com.vf.uk.dal.device.datamodel.merchandisingpromotion;

public class Media {

	private String id;
	private String value;
	private String type;
	private String promoCategory;
	private String offerCode;
	private String description;
	private String discountId;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getPromoCategory() {
		return promoCategory;
	}
	public void setPromoCategory(String promoCategory) {
		this.promoCategory = promoCategory;
	}
	public String getOfferCode() {
		return offerCode;
	}
	public void setOfferCode(String offerCode) {
		this.offerCode = offerCode;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getDiscountId() {
		return discountId;
	}
	public void setDiscountId(String discountId) {
		this.discountId = discountId;
	}
	
	
}
